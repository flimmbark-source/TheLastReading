import assert from 'node:assert/strict';
import { JSDOM } from 'jsdom';

import { installCardDetailGestures } from '../src/ui/cardDetailGestures.mjs';

const dom=new JSDOM('<!doctype html><html><body><div id="hand"><div class="card" data-uid="11"></div></div><div id="spread"><div class="card" data-uid="22"></div></div></body></html>');
const target=dom.window;

const handCard={uid:11,id:'major_1'};
const spreadCard={uid:22,id:'major_2'};
let run={hand:[handCard],spread:[spreadCard,null,null,null,null],selectedCardId:null,busy:false,ability:null,purge:null};
const legacy={hand:[handCard],spread:[spreadCard,null,null,null,null],selected:null,busy:false,abilitySelect:null,purgeSelect:null};
let refreshes=0;
let expanded=null;

target.tlrRuntime={state:legacy};
target.tlrActions={SELECT_CARD:'SELECT_CARD'};
target.tlrStore={
  getState:()=>({run}),
  dispatch(action){
    if(action.type==='SELECT_CARD')run={...run,selectedCardId:action.cardId};
  },
};
target.refreshHandState=()=>{refreshes+=1;};
target.expandCard=card=>{expanded=card;};

function click(element){
  element.dispatchEvent(new target.MouseEvent('click',{bubbles:true,cancelable:true,button:0}));
}

installCardDetailGestures(target);

const handEl=target.document.querySelector('#hand .card');
click(handEl);
assert.equal(expanded,null,'one tap should retain normal card behavior without opening details');
click(handEl);
assert.equal(expanded,handCard,'double tapping a hand card should open that card in the detail view');
assert.equal(run.selectedCardId,11,'double tapping a hand card should leave that card selected');
assert.equal(legacy.selected,11,'legacy selection should stay synchronized');
assert.equal(refreshes,1,'the raised hand-card state should refresh once');
assert.ok(target.__handGestureSuppressClickUntil>target.performance.now(),'the second tap should be consumed');

expanded=null;
target.__handGestureSuppressClickUntil=target.performance.now()+800;
click(handEl);
click(handEl);
assert.equal(expanded,null,'a click generated by dragging should not count toward a double tap');
target.__handGestureSuppressClickUntil=0;

const spreadEl=target.document.querySelector('#spread .card');
click(spreadEl);
assert.equal(expanded,null,'one tap on a spread card should not open details');
click(spreadEl);
assert.equal(expanded,spreadCard,'double tapping a placed spread card should open its detail view');
assert.equal(run.selectedCardId,11,'opening a spread card should not replace the selected hand card');

expanded=null;
run={...run,busy:true};
click(handEl);
click(handEl);
assert.equal(expanded,null,'double tap should stay disabled while the game is busy');

console.log('Card-detail double-tap gesture checks passed.');
